nilai_a = int(input("Masukan Nilai A :"))
nilai_b = int(input("Masukan Nilai B : "))
penjumlahan = nilai_a + nilai_b
pengurangan = nilai_a - nilai_b
perkalian = nilai_a * nilai_b
print(f"Hasil Nilai Jumlah : {penjumlahan}")
print(f"Hasil Nilai Kurang : {pengurangan}")
print(f"Hasil Nilai Bagi : {perkalian}")